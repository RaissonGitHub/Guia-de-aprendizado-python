tupla_numeros = (10, 20, 30, 40, 50)


# Utilizando fatiamento para pegar os três primeiros elementos
# A sintaxe é tupla[inicio:fim], onde 'inicio' é o índice inicial (inclusive) e 'fim' é o índice final (exclusive)
primeiros_tres = tupla_numeros[0:3]


# Imprimindo o resultado
print("Os três primeiros números da tupla são:", primeiros_tres)